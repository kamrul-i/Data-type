package basic_data_type;

public class Data_type {
	public static void main(String[] args) {	
		
        boolean b = true;
        char c = 'a';
        short s = 4578;
        int i = 125;
        float f = 787.87401f;   //float এর মান যা ধরবেন, তার সাথে f না দিলে হবে না ।।
        double d = 787.73120;
                                      
        
        System.out.printf("boolean b = %b\n", b);
        System.out.printf("character c = %c\n", c);
        System.out.printf("short s = %d\n", s);
        System.out.printf("intager i = %d\n", i);
        System.out.printf("floar f = %.2f\n", f);
        System.out.printf("double d = %.2f\n", d);
      
	}

}
